numero1=int(input("Dime un numero -> "))
numero2=int(input("Dime otro numero -> " ))
suma=numero1+numero2
print(f"La suma de \n {numero1:>9,.2f} \n+{numero2:>9,.2f}\n____________\n ={suma:>9,}")
#la letra F es para escribir variables dentro del print
#\n es para escribir abajo de lo que se le escribio (cuidar los espacios tambien)
#>0 es para la cantidad de espacios 
#.2f decimales (...00)
